static void print_right_display(void) {
    // WPM counter
    char wpm_str[4];
    oled_set_cursor(1, 1);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + current_wpm % 10;
    wpm_str[1] = '0' + (current_wpm /= 10) % 10;
    wpm_str[0] = '0' + current_wpm / 10;
    oled_write(wpm_str, false);
    oled_set_cursor(1, 2);
    oled_write("WPM", false);
}

